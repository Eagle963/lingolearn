/**
 * Obtient le mot du jour basé sur la date actuelle
 * Le même mot est affiché toute la journée
 */
export function getWordOfDay(): WordOfDay {
  const today = new Date()
  const dayOfYear = Math.floor(
    (today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / 86400000
  )
  const index = dayOfYear % wordsOfDay.length
  return wordsOfDay[index]
}

/**
 * Obtient un mot aléatoire (pour le mode pratique)
 */
export function getRandomWord(): WordOfDay {
  const index = Math.floor(Math.random() * wordsOfDay.length)
  return wordsOfDay[index]
}
